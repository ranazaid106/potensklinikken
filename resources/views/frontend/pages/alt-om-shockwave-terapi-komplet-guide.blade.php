        <h2 class="blog-2remheading">Hvorfor er shockwave så effektivt mod vaskulær impotens?</h2>
      </div>
      <p class="blog-paragraph margin-bottom-1rem">Hos patienter med sygdom i karsystemet, er karene ofte stive og ufleksible, samt ser man en tilstoppning af de små kar til penis pga. åreforkalkning. Den form for ED er stærkt associeret med metabolsk syndrom. Man ser her en systemisk inflammatorisk tilstand, som  medfører til en reduktion i produktionen af vasodilatorer, især NO (Nitric Oxide).<br></p>
      <p class="blog-paragraph margin-bottom-0">Som beskrevet foroven, så er er der en god synergi for shockwave terapi, da behandlingen har en direkte indvirking i promovering af dannelsen af NO i endotelet. Yderligere ved åreforkalkning, kan shockwave fjerne “snavsen” fra karrene, for at skabe et bedre flow. Oveni får man dannet nye kar, som også er med til at skabe en bedre blodgennemstømning.</p>
