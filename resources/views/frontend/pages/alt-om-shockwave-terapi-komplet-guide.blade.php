      <h3 class="uui-heading-xsmall-2 _1rem">Piezoelektrisk vs Elektromagnetisk</h3>
      <p class="blog-paragraph">Mange klinikker i Danmark anvender maskinen fra Storz, som er baseret på den elektromagnetiske teknologi. Det er det foretrukne valg for rigtig mange kiropraktorer og fysioterapueter, som også er begyndt at anvende den til behandling af ED. I Potensklinikken har vi valgt at bruge Piezowave 2, som bygger på den piezoelektriske teknologi. Fundamentalt set, er der små forskelle i selve chockbølgerne, hvorpå de genereres, men forskningen her er begrænset om hvorvidt den terapeutiske effekt påvirkes.</p>
      <h3 class="uui-heading-xsmall-2 _1rem">Forskellen er i lydhovedet</h3>
