      <p class="blog-paragraph margin-bottom-1rem"><strong>Akutte virkning</strong>: De fleste oplever positiv effekt allerede efter 2-4 behandlinger. Nitrogenoxid (NO), er et vigtigt signalstof i kroppen, der er ansvarlig for både udvidelse af blodårerne og afslapning af musklerne. Med shockwave terapi kan man løsne op i karvæggene, så de har nemmere ved at dumpe og producere NO og derved opnå en bedre og fyldigere rejsning.</p>
      <p class="blog-paragraph"><strong>Langtidsvirkning: </strong>Remodulation og nedlægning af nyt væv tager tid. Processer såsom ny kardannelse (angiogenisis) og collagen syntehsis er noget der udfolder sig langsomt.  Den fulde effekt af terepien opnåes derfor over flere måneder. For at opnå et tilfredsstillende og vedvarende resultat, er det disse underliggende forandringer man prøver at stimulere. Hvor meget man i sidste ende kan fremspire af nye blodkar, kommer an på ens udgangspunkt og antallet af behandlinger, som alt sammen har en forstærkende effekt på resultatet.</p>
