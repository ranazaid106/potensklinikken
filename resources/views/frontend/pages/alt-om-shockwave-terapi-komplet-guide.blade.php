        <h2 class="blog-2remheading">Hvor hurtigt virker shockwave?</h2>
      </div>
      <div class="highlighted-box">
        <div class="highligted-box-text">Der er tale om en akut virkning og en langtidsvirkning. Begge er vigtige og fremmer en bedre udfyldning af penisen.</div>
